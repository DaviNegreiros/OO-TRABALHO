import java.time.LocalDate;


public class Exame{
    String tipo, resultado;
    LocalDate dataPrescricao, dataRealizacao;
    float valor;


    //MÃ©todos
    Exame (String t, String r, LocalDate dP, LocalDate dR,float v){
        tipo = t;
        resultado = r;
        dataPrescricao = dP;
        dataRealizacao = dR;
        valor = v;
    }

    //Read aa
    String getTipo(){
        return tipo;
    }
    String getResultado(){
        return resultado;
    }
    LocalDate getDataPrescricao(){
        return dataPrescricao;
    }
    LocalDate getRealizacao(){
        return dataRealizacao;
    }
    float getValor(){
        return valor;
    }

    //Update
    void setTipo(String a){
        tipo = a;
    }
    void setResultado(String a){
        resultado = a;
    }
    void setDataPrescricao(LocalDate a){
        dataPrescricao = a;
    }
    void setDataRealizacao(LocalDate a){
        dataRealizacao = a;
    }
    void setValor(float a){
        valor = a; 
    }




}